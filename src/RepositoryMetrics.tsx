const RepositoryMetrics = ({ owner, repo, theme }: RepositoryMetricsProps) => {
    const [count, setCount] = useState(null);
    const format = (n: any) => n > 1000 ? `${(n / 1000).toFixed(1)
        }k` : n;


    useEffect(() => {
        (async () => {
            const data = await fetch(`https://api.github.com/repos/${owner}/${repo}`).then(res => res.json());
            setTimeout(() => setCount(data.stargazers_count), 2000); // Using delay for animation
        })();
    }, []);

    return (
        <div className={`github-stargazers theme-${theme} ${count === null ? '' : 'loaded'}`}>
            <a href={`https://github.com/${owner}/${repo}`} className='stargazers-icon'>
                <GithubIcon />
            </a>
            <a href={`https://github.com/${owner}/${repo}/stargazers`} className='stargazers-count'>
                <div>
                    <div>
                        <StarIcon />
                        <span>{count === null ? 0 : format(count)}</span>â˜†
                    </div>
                </div>
            </a>
        </div>
    );
}
